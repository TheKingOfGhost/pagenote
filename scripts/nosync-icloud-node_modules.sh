#!/bin/bash
#basic for command
list=("../manage_web" "../shared" "../sdk" "../web")

for i in ${list[@]};
    do
        oldDir="${i}/node_modules"
        newDir="${i}/node_modules.nosync"

        rm -rf $oldDir
        rm -rf $newDir
        echo create new: "${newDir}"
        mkdir ${newDir}
        ln -s $newDir $oldDir
    done